const DICTS: Record<UiLanguage, Dict> = {
  en: EN,
  "zh-CN": ZH_CN,
};

export function t(lang: UiLanguage, key: string, vars?: Record<string, string | number>): string {
  const template = DICTS[lang][key] ?? DICTS.en[key] ?? key;
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function languageOptions(): Array<{ value: UiLanguage; label: string }> {
  return [
    { value: "en", label: EN.english },
    { value: "zh-CN", label: ZH_CN.chinese_simplified },
  ];
}

export function navLabel(lang: UiLanguage, navId: string): string {
  const key = `nav_${navId.toLowerCase()}`;
  return t(lang, key);
}
